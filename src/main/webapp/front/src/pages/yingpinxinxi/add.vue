<script>
  export default {
    data() {
	  let self = this
      return {
        id: '',
        baseUrl: '',
        ro:{
				gangweimingcheng : false,
				gongzuohuanjing : false,
				gangweifenlei : false,
				xinchoudaiyu : false,
				gongzuodidian : false,
				qiyemingcheng : false,
				gongsidianhua : false,
				gerenjianli : false,
				shenqingyuanyin : false,
				yonghuzhanghao : false,
				yonghuxingming : false,
				lianxifangshi : false,
				shenqingriqi : false,
				sfsh : false,
				shhf : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          gangweimingcheng: '',
          gongzuohuanjing: '',
          gangweifenlei: '',
          xinchoudaiyu: '',
          gongzuodidian: '',
          qiyemingcheng: '',
          gongsidianhua: '',
          gerenjianli: '',
          shenqingyuanyin: '',
          yonghuzhanghao: '',
          yonghuxingming: '',
          lianxifangshi: '',
          shenqingriqi: '',
        },


        rules: {
          gangweimingcheng: [
            { required: true, message: '岗位名称不能为空', trigger: 'blur' },
          ],
          gongzuohuanjing: [
          ],
          gangweifenlei: [
          ],
          xinchoudaiyu: [
          ],
          gongzuodidian: [
          ],
          qiyemingcheng: [
          ],
          gongsidianhua: [
            { validator: this.$validate.isPhone, trigger: 'blur' },
          ],
          gerenjianli: [
          ],
          shenqingyuanyin: [
          ],
          yonghuzhanghao: [
          ],
          yonghuxingming: [
          ],
          lianxifangshi: [
            { validator: this.$validate.isMobile, trigger: 'blur' },
          ],
          shenqingriqi: [
          ],
          sfsh: [
          ],
          shhf: [
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.shenqingriqi = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='gangweimingcheng'){
              this.ruleForm.gangweimingcheng = obj[o];
              this.ro.gangweimingcheng = true;
              continue;
            }
            if(o=='gongzuohuanjing'){
              this.ruleForm.gongzuohuanjing = obj[o].split(",")[0];
              this.ro.gongzuohuanjing = true;
              continue;
            }
            if(o=='gangweifenlei'){
              this.ruleForm.gangweifenlei = obj[o];
              this.ro.gangweifenlei = true;
              continue;
            }
            if(o=='xinchoudaiyu'){
              this.ruleForm.xinchoudaiyu = obj[o];
              this.ro.xinchoudaiyu = true;
              continue;
            }
            if(o=='gongzuodidian'){
              this.ruleForm.gongzuodidian = obj[o];
              this.ro.gongzuodidian = true;
              continue;
            }
            if(o=='qiyemingcheng'){
              this.ruleForm.qiyemingcheng = obj[o];
              this.ro.qiyemingcheng = true;
              continue;
            }
            if(o=='gongsidianhua'){
              this.ruleForm.gongsidianhua = obj[o];
              this.ro.gongsidianhua = true;
              continue;
            }
            if(o=='gerenjianli'){
              this.ruleForm.gerenjianli = obj[o];
              this.ro.gerenjianli = true;
              continue;
            }
            if(o=='shenqingyuanyin'){
              this.ruleForm.shenqingyuanyin = obj[o];
              this.ro.shenqingyuanyin = true;
              continue;
            }
            if(o=='yonghuzhanghao'){
              this.ruleForm.yonghuzhanghao = obj[o];
              this.ro.yonghuzhanghao = true;
              continue;
            }
            if(o=='yonghuxingming'){
              this.ruleForm.yonghuxingming = obj[o];
              this.ro.yonghuxingming = true;
              continue;
            }
            if(o=='lianxifangshi'){
              this.ruleForm.lianxifangshi = obj[o];
              this.ro.lianxifangshi = true;
              continue;
            }
            if(o=='shenqingriqi'){
              this.ruleForm.shenqingriqi = obj[o];
              this.ro.shenqingriqi = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.yonghuzhanghao!=''&&json.yonghuzhanghao) || json.yonghuzhanghao==0){
                this.ruleForm.yonghuzhanghao = json.yonghuzhanghao
            }
            if((json.yonghuxingming!=''&&json.yonghuxingming) || json.yonghuxingming==0){
                this.ruleForm.yonghuxingming = json.yonghuxingming
            }
            if((json.lianxifangshi!=''&&json.lianxifangshi) || json.lianxifangshi==0){
                this.ruleForm.lianxifangshi = json.lianxifangshi
            }
          }
        });

		if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
			localStorage.removeItem('raffleType')
			setTimeout(() => {
				this.onSubmit()
			}, 300)
		}
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`yingpinxinxi/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

			//更新跨表属性
			var crossuserid;
			var crossrefid;
			var crossoptnum;
			this.$refs["ruleForm"].validate(valid => {
				if(valid) {
					if(this.type=='cross'){
						var statusColumnName = localStorage.getItem('statusColumnName');
						var statusColumnValue = localStorage.getItem('statusColumnValue');
						if(statusColumnName && statusColumnName!='') {
							var obj = JSON.parse(localStorage.getItem('crossObj'));
							if(!statusColumnName.startsWith("[")) {
								for (var o in obj){
									if(o==statusColumnName){
										obj[o] = statusColumnValue;
									}
								}
								var table = localStorage.getItem('crossTable');
								this.$http.post(table+'/update', obj).then(res => {});
							} else {
								crossuserid=Number(localStorage.getItem('frontUserid'));
								crossrefid=obj['id'];
								crossoptnum=localStorage.getItem('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
							}
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						var params = {
							page: 1,
							limit: 10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						this.$http.get('yingpinxinxi/list', {
							params: params
						}).then(res => {
							if(res.data.data.total>=crossoptnum) {
								this.$message({
									message: localStorage.getItem('tips'),
									type: 'error',
									duration: 1500,
								});
								return false;
							} else {
								// 跨表计算


								this.$http.post(`yingpinxinxi/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
									if (res.data.code == 0) {
										this.$message({
											message: '操作成功',
											type: 'success',
											duration: 1500,
											onClose: () => {
												this.$router.go(-1);
											}
										});
									} else {
										this.$message({
											message: res.data.msg,
											type: 'error',
											duration: 1500
										});
									}
								});
							}
						});
					} else {


						this.$http.post(`yingpinxinxi/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				}
			});
		},
		// 获取uuid
		getUUID () {
			return new Date().getTime();
		},
		// 返回
		back() {
			this.$router.go(-1);
		},
      gongzuohuanjingUploadChange(fileUrls) {
          this.ruleForm.gongzuohuanjing = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
      gerenjianliUploadChange(fileUrls) {
          this.ruleForm.gerenjianli = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

